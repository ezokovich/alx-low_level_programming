/**
 * op_add - Add number
 *
 * @a: int a
 *
 * @b: int b
 *
 * Return: Return ans
**/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtr  number
 *
 * @a: int a
 *
 * @b: int b
 *
 * Return: Returns result
**/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Mul number
 *
 * @a: int a
 *
 * @b: int b
 *
 * Return: ans
**/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - Div number
 *
 * @a: int a
 *
 * @b: int b
 *
 * Return:  result
**/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo of  number
 *
 * @a: int a
 *
 * @b: int b
 *
 * Return: Returns result
**/

int op_mod(int a, int b)
{
return (a % b);
}

